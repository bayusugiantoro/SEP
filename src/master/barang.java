    protected void autonumber(){
        try {
            String sql = "SELECT kdbarang FROM barang order by kdbarang asc";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            txtkd.setText("BRG0001");
            while (rs.next()) {
                String kd_barang = rs.getString("kdbarang").substring(3);
                int AN = Integer.parseInt(kd_barang) + 1;
                String Nol = ""; 
 
                  if(AN<10)
                  {Nol = "000";}
                  else if(AN<100)
                  {Nol = "00";}
                  else if(AN<1000)
                  {Nol = "0";}
                  else if(AN<10000)
                  {Nol = "";} 
 
                  txtkd.setText("BRG" + Nol + AN);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Auto Number Gagal" +e);
        }
    }

